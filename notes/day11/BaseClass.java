package Day11SmartPhone;

public class BaseClass {
	
	// Inheritance-Getting some actions (methods) from ur parent class is known as ineritance 
	
	public void text()
	{
		System.out.println("Texting Features");
	}
	
	public void calling()
	{
		System.out.println("Calling features");
	}
	
	public void switchon()
	{
		System.out.println("Mobile is switching on");
	}
	
	public void switchoff()
	{
		System.out.println("Mobile is switching off");
	}

	public void torchon()
	{
		System.out.println("Torch is switching on");
	}
	
	public void torchoff()
	{
		System.out.println("Torch is switching off");
	}
}
