package Day13;

public class MethodsforAbstraction extends AbstractLearn {

	@Override
	public void add() {
		
		int a = 10 ;
		int b = 20 ;
		
		System.out.println(a+b);
		
	}

	@Override
	public void sum() {
		int a = 10 ;
		int b = 20 ;
		
		System.out.println(a-b);
		
		
	}

	@Override
	public void div() {
		int a = 10 ;
		int b = 20 ;
		
		System.out.println(a/b);
		
	}

}
